// if statement
// => The if statement is used to execute a block of code only if a specific condition is true.


let age = 5;

if (age === 5) {
    console.log("The age is exactly 5");
}

if (age > 3) {
    console.log("The age is greater than 3");
}

if (age < 10) {
    console.log("The age is less than 10");
}

if (age !== 7) {
    console.log("The age is not 7");
}

if (age >= 5 && age <= 10) {
    console.log("The age is between 5 and 10 inclusive");
}
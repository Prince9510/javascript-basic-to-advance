// Assignment Operators


// Assign
let x = 10;
let y = 5;


// Add and assign	
x += y; // x = x + y
console.log(x); // 15

// Subtract and assign	
x -= y; // x = x - y
console.log(x); // 10

// Multiply and assign	
x *= y; // x = x * y
console.log(x); // 50

// Divide and assign	
x /= y; // x = x / y
console.log(x); // 10

// Modulus and assign	
x %= y; // x = x % y
console.log(x); // 0
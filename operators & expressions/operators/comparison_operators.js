// Comparison Operators

// Equal to (==)	
console.log(5 == '5');  // true


// Equal value & type (===)
console.log(5 === '5'); // false 
console.log(5 === 5); // true 


// Not equal (!=)
console.log(5 != '5');  // false 


// Not equal value or type	(!==)
console.log(5 !== '5'); // true  


// Greater than (>)
console.log(5 > 3);     // true


// Less than (<)	
console.log(5 < 3);     // false


// Greater than or equal to (>=)


console.log(5 >= 5);// true


// Less than or equal to (<=)
console.log(5 <= 3);// false
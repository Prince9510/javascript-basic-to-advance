// Arithmetic Operators

// Addition (+)
let Addition_Value1 = 10;
let Addition_Value2 = 30;

console.log(Addition_Value1 + Addition_Value2); // output : 40

// Subtraction (-)
let Subtraction_Value1 = 50;
let Subtraction_Value2 = 20;

console.log(Addition_Value1 - Addition_Value2); // output : 30

// Multiplication (*)

let Multiplication_Value1 = 50;
let Multiplication_Value2 = 20;

console.log(Multiplication_Value1 * Multiplication_Value2); // output : 100

// Division (/)
let Division_Value1 = 100;
let Division_Value2 = 20;

console.log(Division_Value1 / Division_Value2); // output : 5

// Modulus (%)
let Modulus_Value1 = 29;
let Modulus_Value2 = 5;

console.log(Modulus_Value1 % Modulus_Value2); // output : 4

// Exponentiation (**)
let Exponentiation_Value1 = 2;
let Exponentiation_Value2 = 3;

console.log(Exponentiation_Value1 ** Exponentiation_Value2); // output : 8

// Increment (++)
let Increment_Value = 5;

console.log(Increment_Value++); // output: 5 (post-increment)
console.log(Increment_Value);   // output: 6

Increment_Value = 5; // Resetting value
console.log(++Increment_Value); // output: 6 (pre-increment)

// Decrement (--)
let Decrement_Value = 5;

console.log(Decrement_Value--); // output: 5 (post-decrement)
console.log(Decrement_Value);   // output: 4

Decrement_Value = 5; // Resetting value
console.log(--Decrement_Value); // output: 4 (pre-decrement)